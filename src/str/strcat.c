#include "minishell.h"

void	ft_strcat(char *dest, const char *src)
{
	int	i;

	i = 0;
	while (dest[i] != '\0')
		i++;
	while (*src != '\0')
		dest[i++] = *(src++);
	dest[i] = '\0';
}